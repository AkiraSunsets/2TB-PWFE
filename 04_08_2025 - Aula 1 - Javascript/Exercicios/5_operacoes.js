//peça ao usuário para inserir dois numeros e exiba o resultado das quatro operações matemáticas básicas (soma, subtração, multiplicação e divisão)

let num1 = parseFloat(prompt("Digite o primeiro número: ")); //parsefloat converte a string para um número 
let num2 = parseFloat(prompt("Digite o segundo número: ")); 

alert("Soma: " + (num1 + num2));
alert("Subtração: " + (num1 - num2));
alert("Multiplicação: " + (num1 * num2));
alert("Divisão: " + (num1/ num2));
